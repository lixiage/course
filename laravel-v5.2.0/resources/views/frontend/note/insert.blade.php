        var num=0;
        var arr = [];
        $('.classify').click(function(){
            var str = $(this).attr('class');
            var id = $(this).data('id');
            var state = 0;
            str = str.split(' ');
            for(var i=0;i<str.length;i++){
                if(str[i]=='onactive'){
                    state = 1;
                }
            }
            //判断class中是否已经有classify
            if(state==0){
                //没有
                if(num>=3){
//                    alert('您当前已经选了三个');
                }else{
                    $(this).addClass('onactive');
                    arr[id] = id;
                    num++;
                }
            }
            if(state==1){
                //有
                $(this).removeClass('onactive');
                arr.splice(id,1);
                num--;
            }
        });
    });

